package model;

public class Task {
    private int taskDuration;
    private int taskTimeToDelivery;

    public int getTaskDuration() {
	return taskDuration;
    }

    public void setTaskDuration(int taskDuration) {
	this.taskDuration = taskDuration;
    }

    public int getTaskTimeToDelivery() {
	return taskTimeToDelivery;
    }

    public void setTaskTimeToDelivery(int taskTimeToDelivery) {
	this.taskTimeToDelivery = taskTimeToDelivery;
    }

}
